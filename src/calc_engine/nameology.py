    def _calc_wuge(self, surname: str, given_name: str, gender: str) -> Dict:
        """计算五格"""
        # 天格: 姓之画数+1 (先天)
        tian_strokes = self._get_strokes(surname) + 1

        # 人格: 姓之末笔 + 名之首笔
        ren_strokes = self._get_strokes(surname[-1]) + (self._get_strokes(given_name[0]) if given_name else 0)

        # 地格: 名之总画数
        di_strokes = sum(self._get_strokes(c) for c in given_name)

        # 总格: 姓名总画数
        total_strokes = sum(self._get_strokes(c) for c in (surname + given_name))

        # 外格: 总格-人格+1
        wai_strokes = total_strokes - ren_strokes + 1

        def mod81(n):
            return ((n % 81) or 81)

        return {
            "天格数": mod81(tian_strokes),
            "人格数": mod81(ren_strokes),
            "地格数": mod81(di_strokes) if di_strokes > 0 else 0,
            "外格数": mod81(wai_strokes),
            "总格数": mod81(total_strokes),
            "姓笔画": self._get_strokes(surname),
            "名笔画": [self._get_strokes(c) for c in given_name],
        }

