    async def calculate(self, birth_datetime, birth_location, gender, query_scene,
                     query_time=None, name=None, **kwargs):
        import time as _t
        t0 = _t.time()
        name = name or kwargs.get("surname", "") or kwargs.get("full_name", "")
        ck = self._get_cache_key(system=self.name, dt=birth_datetime.isoformat(),
                                  scene=query_scene, query=name or "none")
        if ck in self._cache:
            r = self._cache[ck]; r.cached = True; return r

        if not name:
            return CalculationResult(system=self.name, score=50.0, confidence=0.30,
                trend="neutral", risk_level="low",
                details={"说明": "未提供姓名，无法分析", "五格": {}},
                calculation_time_ms=int((_t.time()-t0)*1000))

        # 姓名分字
        chars = list(name)
        surname = chars[0] if chars else ""
        given_name = "".join(chars[1:]) if len(chars) > 1 else ""

        # 五格计算
        wu_ge = self._calc_wuge(surname, given_name, gender)

        # 三才
